var roleUpgrader = {

    /** @param {Creep} creep **/
    run: function(creep) {

        if (!creep.room.controller.sign || creep.room.controller.sign.username != "RealNerdEthan") {
            if (creep.signController(creep.room.controller, "github.com/RealNerdEthan") == ERR_NOT_IN_RANGE) {
                creep.moveTo(creep.room.controller);
                return;
            }
        }
        
        if(creep.memory.upgrading && creep.store[RESOURCE_ENERGY] == 0) {
            creep.memory.upgrading = false;
            creep.say('ðŸ”„ harvest');
        }

        if(!creep.memory.upgrading && creep.store.getFreeCapacity() == 0) {
            creep.memory.upgrading = true;
            creep.say('âš¡ upgrade');
        }

        if(creep.memory.upgrading) {
            if(creep.upgradeController(creep.room.controller) == ERR_NOT_IN_RANGE) {
                creep.moveTo(creep.room.controller, {visualizePathStyle: {stroke: '#00FFFF'}});
            }
        }
        else {
            var sources = creep.pos.findClosestByRange(FIND_SOURCES);
            if(creep.harvest(sources) == ERR_NOT_IN_RANGE) {
                creep.moveTo(sources, {visualizePathStyle: {stroke: '#00FFFF'}});
            }
        }
    }
};

module.exports = roleUpgrader;